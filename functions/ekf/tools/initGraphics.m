% Initialise graphics handles.
function Graphics = initGraphics(World, Obstacles, handles)

    Graphics.Map = line('parent',handles.axes1,...
        'linestyle','none',...
        'marker','s',...
        'MarkerFaceColor', 'y', ...
        'color','y',...
        'xdata',[],...
        'ydata',[]);
    
    Graphics.World = line('parent',handles.axes1,...
        'linestyle','none',...
        'marker','o',...
        'color','b',...
        'xdata',World.W(1,:),...
        'ydata',World.W(2,:));

    Graphics.TrueRob = line('parent',handles.axes1,...
        'color','b',...
        'xdata',[],...
        'ydata',[]);
    
    Graphics.Rob = line('parent',handles.axes1,...
        'color','r',...
        'xdata',[],...
        'ydata',[]);

    Graphics.Lmk = line('parent',handles.axes1,...
        'linestyle','none',...
        'marker','+',...
        'color','g',...
        'xdata',[],...
        'ydata',[]);

    Graphics.LmkLines = line(...
        'parent',handles.axes1, ...
        'color','g', ...
        'xdata',[], ...
        'ydata',[]);

    Graphics.Ellipse = zeros(1,size(World.W,2));
    for i = 1:numel(Graphics.Ellipse)
        Graphics.Ellipse(i) = line(...
            'parent', handles.axes1,...
            'color','r',...
            'xdata',[],...
            'ydata',[]);
    end

    Graphics.RobCov = line(...
        'parent', handles.axes1,...
        'color','r',...
        'xdata',[],...
        'ydata',[]);
    
    if ~isempty(Obstacles)
        for i = 1:length(Obstacles)
            Obstacles(i).graphics = [];
            Obstacles(i).plot(handles.axes1);
        end
    end
    
    Graphics.Scan = line('parent',handles.axes1,...
        'linestyle','none',...
        'marker','x',...
        'color','r',...
        'xdata',[],...
        'ydata',[]);
    
    Graphics.R_hist = line('xdata', [], ...
        'ydata', [], ...
        'color', 'b', ...
        'parent', handles.axes1);

    Graphics.r_hist = line('xdata', [], ...
        'ydata', [], ...
        'color', 'r', ...
        'parent', handles.axes1);
    
    Graphics.map1 = imagesc([], ...
        'parent', handles.map1);
    colormap(gray);
    axis equal
    
    Graphics.map2 = imagesc([], ...
        'parent', handles.map2);
    colormap(gray);
    axis equal

end